	<h3>Para que servem as FAQs?</h3>
	<p>FAQs é a sigla de Frequently Asked Questions, que significa Perguntas frequentes. Esta sessão evita que a mesma dúvida por parte de vários membros seja respondida num único sitio.</p>
	
	<h3>Qual o objectivo de NexuSystem?</h3>
	<p>Apoiar o objectivo do projecto NexuS, a criação de um jogo criado em Portugal, por portugueses.</p>
	
	<h3>Como puderei passar a Utilizador Mais?</h3>
	<p>É muito fácil, bastará ter 50 posts no fórum, depois basta ir ás tuas informações de conta e aparecerá um botão para fazeres Upgrade à tua conta Utilizador.</p>
	
	<h3>Como posso passar a VIP?</h3>
	<p>Para isso deverás contactar a administração através do endereço de email <a href="mailto:geral@nexusystem.com?subject=Conta-VIP">geral@nexusystem.com</a> e pedir as instruções de como proceder. O valor da conta VIP é de 250 MO/Mês.</p>
	
	<h3>Qual o nome do jogo?</h3>
	<p>O jogo presente neste site é chamado "NexuS : O Legado" em português, ou "NexuS : The Legacy" em inglês.</p>
	
	<h3>O que é um Nexar?</h3>
	<p>É um habitante de NexuS, sendo um humanoide como nós humanos, mas com uma diferenção a nivel de controlo de elementos, pois podem usar 9 elementos diferentes como defesa ou como ataque.</p>
	
	<h3>Quais os elementos em que se basearam?</h3>
	<p>Sendo eles 9: Água, Fogo, Erva/Terra, Ar/Vento, Luz, Escuridão, Psíquico, Electricidade e Normal.</p>
	
	<h3>Como criar uma conta no jogo?</h3>
	<p>Sobrepôe teu rato ao menu <b>Conta</b>, e clica na opção <b>Aplicações</b>, procura a aplicação <b>NexuS : O Legado</b>, e insere os dados que são pedidos.</p>
	
	<h3>Como posso ganhar Moedas de Ouro com referidos?</h3>
	<p>O processo actual necessita que a pessoal à qual pretendas fazer o convite ainda não possua uma conta em jogo.<br>Indica à pessoa para criar uma conta na comunidade, e de seguida para fazer login, e entrega-lhe o link apresentado na página Principal do jogo, irá direccionar a pessoa para a área de criação de um Nexar já com a referência à tua conta.</p>
